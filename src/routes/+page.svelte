<script lang="ts">
 import { onMount } from 'svelte';
 import { supabase } from '$lib/supabaseClient';

 // ١. گۆڕاوێن سەرەکی
 let doctorId = $state(0);
 let isLoading = $state(true);
 
 // هەلبژارتنا هەیڤ و ساڵێ
 let selectedMonth = $state(new Date().getMonth() + 1); // هەیڤا نوکە
 let selectedYear = $state(new Date().getFullYear());

 // ئامارێن هەیڤا هەلبژارتی
 let monthlyStats = $state({
  patients: 0,
  income: 0,
  expenses: 0,
  profit: 0
 });

 let recentActivity = $state<any[]>([]);

 const months = [
  { id: 1, name: 'January' }, { id: 2, name: 'February' }, { id: 3, name: 'March' },
  { id: 4, name: 'April' }, { id: 5, name: 'May' }, { id: 6, name: 'June' },
  { id: 7, name: 'July' }, { id: 8, name: 'August' }, { id: 9, name: 'September' },
  { id: 10, name: 'October' }, { id: 11, name: 'November' }, { id: 12, name: 'December' }
 ];

 onMount(async () => {
  const storedId = localStorage.getItem('doctor_id');
  if (storedId) {
   doctorId = Number(storedId);
   await loadDashboard();
  }
 });

 // 📊 فانکشنا سەرەکی بۆ ئینانا ئامارێن هەر هەیڤەکێ
 async function loadDashboard() {
  isLoading = true;
  
  // دیارکرنا دەسپێک و دووماهیا هەیڤا هەلبژارتی
  const startDate = new Date(selectedYear, selectedMonth - 1, 1).toISOString();
  const endDate = new Date(selectedYear, selectedMonth, 0, 23, 59, 59).toISOString();

  // ١. ئینانا داهاتێ هەیڤێ (Income from Medical Records)
  const { data: records } = await supabase.from('medical_records')
   .select('fee, created_at, diagnosis, patients(name)')
   .eq('doctor_id', doctorId)
   .gte('created_at', startDate)
   .lte('created_at', endDate);

  // ٢. ئینانا خەرجیێن هەیڤێ (Expenses)
  const { data: exps } = await supabase.from('expenses')
   .select('amount')
   .eq('doctor_id', doctorId)
   .gte('created_at', startDate)
   .lte('created_at', endDate);

  // حسابکرن
  if (records) {
   monthlyStats.patients = records.length;
   monthlyStats.income = records.reduce((s, r) => s + (Number(r.fee) || 0), 0);
   recentActivity = records.slice(-5).reverse();
  } else {
   monthlyStats.patients = 0; monthlyStats.income = 0;
  }

  if (exps) {
   monthlyStats.expenses = exps.reduce((s, e) => s + (Number(e.amount) || 0), 0);
  } else {
   monthlyStats.expenses = 0;
  }

  monthlyStats.profit = monthlyStats.income - monthlyStats.expenses;
  isLoading = false;
 }

 // 🖨️ چاپکرنا ڕاپۆرتا پڕۆفیشناڵ یا هەیڤانە
 function printMonthlyReport() {
  const monthName = months.find(m => m.id === selectedMonth)?.name;
  const win = window.open('', '', 'width=900,height=1000');
  win?.document.write(`
   <div style="font-family:sans-serif; padding:50px; border:2px solid #4f46e5; border-radius:15px;">
    <h1 style="text-align:center; color:#4f46e5; margin-bottom:5px;">Monthly Performance Report</h1>
    <p style="text-align:center; margin-top:0;">${monthName} ${selectedYear}</p>
    <hr>
    <div style="display:grid; grid-template-columns:1fr 1fr; gap:20px; margin:30px 0;">
     <div style="padding:15px; background:#f0f9ff; border-radius:10px;"><b>Total Patients:</b> ${monthlyStats.patients}</div>
     <div style="padding:15px; background:#f0fdf4; border-radius:10px;"><b>Monthly Income:</b> $${monthlyStats.income}</div>
     <div style="padding:15px; background:#fef2f2; border-radius:10px;"><b>Monthly Expenses:</b> $${monthlyStats.expenses}</div>
     <div style="padding:15px; background:#eef2ff; border-radius:10px;"><b>Net Profit:</b> $${monthlyStats.profit}</div>
    </div>
    <p style="text-align:center; color:#666; font-size:12px;">Generated by E-Clinic Digital System</p>
   </div>
  `);
  win?.document.close();
  win?.print();
 }
</script>

<div class="dashboard">
 <!-- Top Control Bar -->
 <header class="dashboard-header">
  <div class="title-area">
   <h1>Performance Overview 🚀</h1>
   <p>ئامارێن هوور یێن کلینیکێ ل دویڤ هەیڤان</p>
  </div>
  
  <div class="controls">
   <select bind:value={selectedMonth} onchange={loadDashboard}>
    {#each months as m}<option value={m.id}>{m.name}</option>{/each}
   </select>
   <select bind:value={selectedYear} onchange={loadDashboard}><option value={2025}>2025</option>
    <option value={2026}>2026</option>
   </select>
   <button class="print-btn" onclick={printMonthlyReport}>🖨️ Print Report</button>
  </div>
 </header>

 {#if isLoading}
  <div class="loader">Loading Analytics...</div>
 {:else}
  <!-- Stats Grid -->

    </div>
   </div>
   <div class="stat-card green">
    <div class="icon-bg">💰</div>
    <div class="info">
     <span>Total Income</span>
     <h3>${monthlyStats.income}</h3>
    </div>
   </div>
   <div class="stat-card red">
    <div class="icon-bg">💸</div>
    <div class="info">
     <span>Expenses</span>
     <h3>${monthlyStats.expenses}</h3>
    </div>
   </div>
   <div class="stat-card gold">
    <div class="icon-bg">📈</div>
    <div class="info">
     <span>Net Profit</span>
     <h3>${monthlyStats.profit}</h3>
    </div>
   </div>
  </div>

  <div class="main-grid">
   <!-- Comparison Chart (CSS Based) -->
   <div class="card glass chart-card">
    <h3>📊 Income vs Expenses</h3>
    <div class="bar-container">
     <div class="bar-row">
      <!-- svelte-ignore a11y_label_has_associated_control -->
      <label>Income</label>
      <div class="bar-bg"><div class="bar-fill green" style="width: {monthlyStats.income > 0 ? '100%' : '0%'}"></div></div>
      <span class="val">${monthlyStats.income}</span>
     </div>
     <div class="bar-row">
      <!-- svelte-ignore a11y_label_has_associated_control -->
      <label>Expenses</label>
      <div class="bar-bg"><div class="bar-fill red" style="width: {(monthlyStats.expenses / (monthlyStats.income || 1)) * 100}%"></div></div>
      <span class="val">${monthlyStats.expenses}</span>
     </div>
    </div>
    <p class="profit-footer">The profit margin for this month is <b>${monthlyStats.profit}</b></p>
   </div>

   <!-- Recent Log -->
   <div class="card glass list-card">
    <h3>🕒 Recent Month Activity</h3>
    <div class="activity-list">
     {#each recentActivity as item}
      <div class="item">
       <div class="avatar">{item.patients?.name[0]}</div>
       <div class="details">
        <b>{item.patients?.name}</b>
        <span>{item.diagnosis}</span>
       </div>
       <div class="price">+${item.fee}</div>
      </div>
     {:else}
      <p class="empty">No records for this month.</p>
     {/each}
    </div>
   </div>
  </div>
 {/if}
</div>

